import UIKit

extension UIColor {
    
    static func softRed() -> UIColor {
        return UIColor(red: 225/255.0, green: 76/255.0, blue: 47/255.0, alpha: 1)
    }
    
    static func separator() -> UIColor {
        return UIColor(red: 236/255.0, green: 236/255.0, blue: 236/255.0, alpha: 1)
    }
    
    static func offWhite() -> UIColor {
        return UIColor(red: 248/255.0, green: 243/255.0, blue: 240/255.0, alpha: 1)
    }
    
    static func shadow() -> UIColor {
        return UIColor(red:225.0/255.0, green:228.0/255.0, blue:228.0/255.0, alpha:1.0)
    }
    
    static func softGrey() -> UIColor {
        return UIColor(red: 216/255.0, green: 216/255.0, blue: 216/255.0, alpha: 1)
    }
    
    static func deepBlue() -> UIColor {
        //52, 152, 219 - curious blue
        //82, 179, 217 - shakespeare
        return UIColor(red: 65/255.0, green: 110/255.0, blue: 177/255.0, alpha: 1)
    }
    
    static func lightGreen() -> UIColor {
        return UIColor(red: 125/255.0, green: 211/255.0, blue: 124/255.0, alpha: 1)
    }
    
    static func jade() -> UIColor {
        return UIColor(red: 21/255.0, green: 165/255.0, blue: 55/255.0, alpha: 1)
    }
    
}
